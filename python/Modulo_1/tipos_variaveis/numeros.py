#Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

#real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

#type() - Retorna qual é o tipo da variável trabalhada
print("Tipo da variável inteiro = ",type(numero_inteiro))
print("Tipo da variável Real Flutuante = ",type(numero_real))

#soma +
soma = 1+1
print("função soma 1 + 1 =  = ",soma)

#subtralção -
subtracao = 100-1
print("função subtracao 100 - 1 =  ",subtracao)

#multiplicação *
multiplicacao = 100*2
print("função multiplicacao 100 * 2 =  ",multiplicacao)

#divisão /
divisao = 100/2
print("função divisao 100 / 2 = ", divisao)


#divisão inteiro /
divisao_inteiro = 5 // 2
print("função divisao retornando inteiro 5 // 2 = ", divisao_inteiro)

#converter de float para inteiro
print("Transformando em inteiro com int()", int(divisao))
print("Tipo ", type(int(divisao)))


#converter de inteiro para float
print("Transformando em inteiro com float()", float(soma))
print("Tipo ", type(float(soma)))

#Mudulo %
modulo = 5 % 2
print("Resultado do modulo de 5 por 2 =  ",modulo)

